package pojo;

import java.sql.Date;

public class All {
	
	private Date DATE;
	private Double HMD;
	private Double PCP;
	private Integer SLR;
	private Integer MAX;
	private Integer MIN;
	private Double WND;
	
	public Date getDATE() {
		return DATE;
	}
	public void setDATE(Date dATE) {
		DATE = dATE;
	}
	public Double getHMD() {
		return HMD;
	}
	public void setHMD(Double hMD) {
		HMD = hMD;
	}
	public Double getPCP() {
		return PCP;
	}
	public void setPCP(Double pCP) {
		PCP = pCP;
	}
	public Integer getSLR() {
		return SLR;
	}
	public void setSLR(Integer sLR) {
		SLR = sLR;
	}
	public Integer getMAX() {
		return MAX;
	}
	public void setMAX(Integer mAX) {
		MAX = mAX;
	}
	public Integer getMIN() {
		return MIN;
	}
	public void setMIN(Integer mIN) {
		MIN = mIN;
	}
	public Double getWND() {
		return WND;
	}
	public void setWND(Double wND) {
		WND = wND;
	}
	
}
